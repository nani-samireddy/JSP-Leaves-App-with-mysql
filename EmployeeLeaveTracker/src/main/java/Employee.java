
public class Employee {
	String name;
	String signum;

	public Employee(String name, String signum) {
		this.name = name;
		this.signum = signum;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSignum() {
		return signum;
	}

	public void setSignum(String signum) {
		this.signum = signum;
	}

}